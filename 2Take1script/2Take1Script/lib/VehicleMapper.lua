function VehicleMapper.GetVehicles(Model, Name, CaseInsensitive)
	if CaseInsensitive then
		if Model then Model = Model:lower() end
		if Name then Name = Name:lower() end
	end
	local idx = 0
	return function()
		while idx < #Vehicles do
			idx = idx + 1
			local hash, model, name = Vehicles[idx].Hash, Vehicles[idx].Model, Vehicles[idx].Name
			if (Model == nil or (CaseInsensitive and model:lower() or model):find(Model, 1, true)) and (Name == nil or (CaseInsensitive and name:lower() or name):find(Name, 1, true)) then
				return hash, model, name
			end
		end
		return nil
	end
end

function VehicleMapper.CheckHashes()
	local valid_hashes = vehicle.get_all_vehicle_model_hashes()
	local missing = {}
	local invalid = {}
	for i=1,#valid_hashes do
		if VehicleMapper.GetModelFromHash(valid_hashes[i]) == nil then
			missing[#missing + 1] = valid_hashes[i]
		end
	end
	for i=1,#Vehicles do
		local found = false
		for j=1,#valid_hashes do
			if Vehicles[i].Hash == valid_hashes[j] then
				found = true
				break
			end
		end
		if not found then invalid[#invalid + 1] = Vehicles[i].Hash end
	end
	return missing, invalid
end

