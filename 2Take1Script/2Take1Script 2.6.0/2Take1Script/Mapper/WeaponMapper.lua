function Weapons.GetHashFromName(Name)
    for i = 1, #Weapons do
        local WeaponCategory = Weapons[i]

        for j = 1, #WeaponCategory.Children do
            local CurrentWeapon = WeaponCategory.Children[j]
            
            if CurrentWeapon.Name == Name then
                return CurrentWeapon.Hash
            end

        end
    end

    return 0
end

function Weapons.GetNameFromHash(Hash)
    for i = 1, #Weapons do
        local WeaponCategory = Weapons[i]

        for j = 1, #WeaponCategory.Children do
            local CurrentWeapon = WeaponCategory.Children[j]
            
            if CurrentWeapon.Hash == Hash then
                return CurrentWeapon.Name
            end

        end
    end

    return 0
end

