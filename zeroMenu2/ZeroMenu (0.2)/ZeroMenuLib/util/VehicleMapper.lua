  for i=1,#Vehicles do
    local veh = Vehicles[i]
    if veh.Model == Model then return veh.Hash end
  end
  return nil
end

function VehicleMapper.GetNameFromModel(Model)
  if Model == nil or type(Model) ~= "string" or Model:len() == 0 then return nil end
  for i=1,#Vehicles do
    local veh = Vehicles[i]
    if veh.Model == Model then return veh.Name end
  end
  return nil
end

function VehicleMapper.FromName(Name)
  if Name == nil or type(Name) ~= "string" or Name:len() == 0 then return nil end
  for i=1,#Vehicles do
    local veh = Vehicles[i]
    if veh.Name == Name then return veh.Hash, veh.Model end
  end
  return nil
end

function VehicleMapper.GetHashFromName(Name)
  if Name == nil or type(Name) ~= "string" or Name:len() == 0 then return nil end
  for i=1,#Vehicles do
    local veh = Vehicles[i]
    if veh.Name == Name then return veh.Hash end
  end
  return nil
end

function VehicleMapper.GetModelFromName(Name)
  if Name == nil or type(Name) ~= "string" or Name:len() == 0 then return nil end
  for i=1,#Vehicles do
    local veh = Vehicles[i]
    if veh.Name == Name then return veh.Model end
  end
  return nil
end

function VehicleMapper.GetVehicles(Model, Name, CaseInsensitive)
  if CaseInsensitive then
    if Model then Model = Model:lower() end
    if Name then Name = Name:lower() end
  end
  local idx = 0
  return function()
    while idx < #Vehicles do
      idx = idx + 1
      local hash, model, name = Vehicles[idx].Hash, Vehicles[idx].Model, Vehicles[idx].Name
      if (Model == nil or (CaseInsensitive and model:lower() or model):find(Model, 1, true)) and (Name == nil or (CaseInsensitive and name:lower() or name):find(Name, 1, true)) then
        return hash, model, name
      end
    end
    return nil
  end
end

return VehicleMapper